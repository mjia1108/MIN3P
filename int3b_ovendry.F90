!c ----------------------------------------------------------------------
!c subroutine velovapour
!c compute velocities in the vapour phase 
!c ----------------------------------------------------------------------
real*8 function int3b_ovendry(w,wm,m) 

implicit none 

real*8, intent(in)    :: w

real*8, intent(in)    :: wm

real*8, intent(in)    :: m
       

int3b_ovendry = m*((w-wm)+wm*dlog(wm)-w*dlog(w))

return
end function 